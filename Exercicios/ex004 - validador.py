algo = input('digite algo ')

print('Tipo: ', type(algo))
print('É alphaNumerico? ', algo.isalnum())
print('É decimal? ', algo.isdecimal())
print('É numerico? ', algo.isnumeric())
print('Esta em minusculo? ', algo.islower())
print('Esta reservado? ', algo.isidentifier())
print('Esta em maiusculo? ', algo.isupper())
